public class Calculator {

    public int add(int x, int y){
        return x+y;
    }

    public int dif(int x, int y){
        return x-y;
    }

    public int multiply(int x, int y){
        return x*y;

    }
    public int divide(int x, int y){
        return x/y;
    }

    //Restul impartirii a doua numere

    public int rest(int x, int y){
        return (x%y);
    }


}
